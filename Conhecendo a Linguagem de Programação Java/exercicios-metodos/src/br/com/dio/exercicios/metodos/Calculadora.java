package br.com.dio.exercicios.metodos;

public class Calculadora {

    public static void Somar(double n1, double n2){

        System.out.println("Resultado da soma: "+ (n1 + n2));

    }
    public static void Subtrair(double n1, double n2){

        System.out.println("Resultado da subtração: "+ (n1 - n2));

    }
    public static void Multiplicar(double n1, double n2){

        System.out.println("Resultado da multiplicação: "+ (n1 * n2));

    }
    public static void Dividir(double n1, double n2){

        System.out.println("Resultado da divisão: "+ (n1 / n2));

    }
    
}
